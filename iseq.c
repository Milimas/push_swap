#include "push_swap.h"

int		iseq(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	while (*s1 && *s2)
		if (*s1++ != *s2++)
			return (0);
	if (*s1 == *s2)
		return (1);
	return (0);
}
